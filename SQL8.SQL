-- INNER JOIN  ---> jo common values hai unko show krta hai
-- syntax 1
SELECT * FROM STUDENT
INNER JOIN DEPARTMENT
ON STUDENT.DEPT_ID = DEPARTMENT.ID;

-- syntax 2
SELECT * FROM STUDENT,DEPARTMENT
WHERE STUDENT.DEPT_ID = DEPARTMENT.ID;

-- syntax 3
SELECT S.ID , S.NAME , S.MARKS , D.DEPT_NAME
FROM STUDENT S
INNER JOIN DEPARTMENT D
ON S.DEPT_ID = D.ID;


-- LEFT JOIN   ---> JO LEFT SIDE KA TABLE HOGA UNKA DATA SHOW KRTE HAI OR JO RIGHT MEIN EXTRA HOGA USKI JGH NULL VALUES SHOW KRTE HAI
-- syntax 1
SELECT * FROM STUDENT
LEFT JOIN DEPARTMENT
ON STUDENT.DEPT_ID = DEPARTMENT.ID;

-- syntax 2
SELECT S.ID , S.NAME , S.MARKS , D.DEPT_NAME
FROM STUDENT S
LEFT JOIN DEPARTMENT D
ON S.DEPT_ID = D.ID;


-- RIGHT JOIN   -->- JO RIGHT SIDE KA TABLE HOGA UNKA DATA SHOW KRTE HAI OR JO LEFT MEIN EXTRA HOGA USKI JGH NULL VALUES SHOW KRTE HAI
-- syntax 1
SELECT * FROM STUDENT
RIGHT JOIN DEPARTMENT
ON STUDENT.DEPT_ID = DEPARTMENT.ID;

-- syntax 2
SELECT S.ID , S.NAME , S.MARKS , D.DEPT_NAME
FROM STUDENT S
RIGHT JOIN DEPARTMENT D
ON S.DEPT_ID = D.ID;



